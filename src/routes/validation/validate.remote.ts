export const getUnitData = form(
	v.object({ listFile: v.file(), selectedEra: v.string(), selectedFaction: v.string(), rules: v.string() }),
	async ({ listFile, selectedEra, selectedFaction, rules }) => {
		const era = Number(selectedEra);
		const faction = Number(selectedFaction);
		let unitData: ValidationUnitData[] = [];
		let bfsData: ValidationBFSData[] = [];

		const buffer = await listFile.arrayBuffer();
		const pdf = await getDocument(buffer).promise;
		const metadata = (await pdf.getMetadata()).info;
		const page = await pdf.getPage(1);
		const content = await page.getTextContent();

		const parsedData = getUnitDataFromPDF(content, metadata);
		if (parsedData.status == "failed")
			return { status: "failed", message: "Invalid PDF Format. Either it wasn't generated by the MUL or this site, or my reader is broken somehow." };
		if (parsedData.status == "ongoing")
			return { status: "failed", message: "PDF format will be supported soon, but is currently in progress. Please upload a pdf generated from the MUL" };

		for (const parsedUnit of parsedData.data?.parsedUnitData ?? []) {
			const unit = await getMULDataFromName(parsedUnit.name);
			if (unit) {
				let unique: boolean | undefined = await isUnitUnique({ unitId: unit.id, eras: [era] });
				const general = await getGeneralId({ era, faction });
				let available: boolean | undefined = (await isUnitAvailable({ unitId: unit.id, eras: [era], factions: [faction, general ?? 0] })) || unit.mulId < 0;

				unitData.push({
					id: crypto.randomUUID(),
					name: parsedUnit.name,
					skill: parsedUnit.skill,
					pv: parsedUnit.pv,
					mulData: unit,
					link: unit ? `http://masterunitlist.info/Unit/Details/${unit.mulId}` : undefined,
					unique: unique,
					available: available
				});
			} else {
				unitData.push({
					id: nanoid(),
					name: parsedUnit.name,
					skill: parsedUnit.skill,
					pv: parsedUnit.pv
				});
			}
		}
		for (const parsedBfs of parsedData.data?.parsedBfsData ?? []) {
			const bfsPacks = getBFSPacks(getRulesByName(rules)?.bfs?.allowedPacks ?? []);
			const bfs = bfsPacks
				.values()
				.flatMap((bfs) => bfs)
				.find((bfs) => bfs.name == parsedBfs.name);
			bfsData.push({
				id: bfs?.id,
				name: parsedBfs.name,
				count: parsedBfs.count,
				pv: bfs?.pvCost,
				bsp: bfs?.bspCost,
				available: bfs?.id != undefined ? true : false
			});
		}
		await nothing().refresh();
		return { status: "success", data: { unitData, bfsData, formationCount: 0, scaCount: 0 } };
	}
);

export const getPossibleUnitList = form(v.object({ searchTerm: v.string() }), async ({ searchTerm }) => {
	const results = await prisma.unit.findMany({ where: { name: { contains: searchTerm } }, select: { mulId: true, name: true } });

	await nothing().refresh();
	return results;
});

export const getSingleUnitData = form(
	v.object({ mulId: v.string(), eraId: v.string(), factionId: v.string(), unitSkill: v.number() }),
	async ({ mulId, eraId, factionId, unitSkill }) => {
		const mulData = await getMULDataFromId(Number(mulId));
		if (mulData) {
			const skill = Number(unitSkill);
			const era = Number(eraId);
			const faction = Number(factionId);

			await nothing().refresh();
			if (mulData == undefined) {
				return { status: "failed", message: "Invalid Unit Id" };
			} else {
				const unique = await isUnitUnique({ unitId: mulData.id, eras: [era] });
				const general = await getGeneralId({ era, faction });
				const available = await isUnitAvailable({ unitId: mulData.id, eras: [era], factions: [faction, general ?? 0] });

				let unitData: ValidationUnitData = {
					id: crypto.randomUUID(),
					name: mulData.name,
					skill,
					pv: getNewSkillCost(skill, mulData.pv),
					mulData: mulData,
					link: mulData.mulId > 0 ? `http://masterunitlist.info/Unit/Details/${mulData.mulId}` : undefined,
					unique,
					available
				};
				return { status: "success", data: unitData };
			}
		}
	}
);
